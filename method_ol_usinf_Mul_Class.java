class Adder{

    static int sum(int a, int b)
    {
        return a+b;
    }
    static int sum (int a , int b , int c)
    {
        return a+b+c;
    }

}

public class method_ol_usinf_Mul_Class {
    public static void main(String[] args) {
        
            System.out.println(Adder.sum(10,10));
            System.out.println(Adder.sum(10,10,10));
    }
    
}
